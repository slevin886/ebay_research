function populateCategories(obj, categorySelect) {
 Object.keys(obj).forEach(function(key) {
  categorySelect.options[categorySelect.options.length]  = new Option(key, obj[key]['code']);
 })
}

function clearOptions(selectBox){
 let i;
  for(i = selectBox.options.length - 1 ; i >= 0 ; i--) {
      selectBox.remove(i);
  }
}

function addSubCategories() {
 let showSub = document.getElementById('showSubCategory');
 showSub.style.display = 'none';
 let obj = document.getElementById('category_select');
 let text = obj.options[obj.selectedIndex].text;
 const subCategories = categoryIds[text]['sub_categories'];
 if (!(subCategories === undefined) && !(subCategories.length === 0)) {
  let subCats = document.getElementById('sub_category_select');
  clearOptions(subCats);

  subCats.options[subCats.options.length]  = new Option('No Subcategory', '');

  subCategories.forEach(function(cat) {
   subCats.options[subCats.options.length]  = new Option(cat['name'], cat['code']);
   }
  );

  showSub.style.display = 'block';
 }
}

export function categoryInitiation() {
 let categorySelect = document.getElementById('category_select');
 populateCategories(categoryIds, categorySelect);
 categorySelect.addEventListener('change', addSubCategories)
}
